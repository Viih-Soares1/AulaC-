using System;

class Program{
    static void Main(){             
        Console.WriteLine("A potência de 2 elevado a 8 é " + Math.Pow(2,8)); //POTêNCIA
        Console.WriteLine("A raiz quadrada de 121 é " + Math.Sqrt(121)); // RAIZ QUADRADA
        Console.WriteLine(Math.Round(3.4)); // Arredonda o numero ao mais próximo
        Console.WriteLine(Math.Ceiling(3.4)); // Arredonda para cima
        Console.WriteLine(Math.Floor(3.4)); // Arredonda pra baixo
        Console.WriteLine(Math.Min(6,7)); // Minimo
        Console.WriteLine(Math.Max(3,7)); // maximo
    }
}