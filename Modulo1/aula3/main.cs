using System;

class Program{
    static void Main(){
        int n1 = 10;
        int n2 = 5;

        Console.WriteLine(n1 + n2 ); // ADIÇÃO
        Console.WriteLine(n1 - n2 ); // SUBTRAÇÃO
        Console.WriteLine(n1 * n2 ); // MULTIPLICAÇÃO
        Console.WriteLine(n1 / n2 ); // DIVISÃO
        Console.WriteLine(n1 % n2 ); // RESTO DA DIVISÃO
        Console.WriteLine(-n1); // NEGATIVO
        n1++; // ACRESCENTA 1 AO VALOR DA VARIAVEL
        Console.WriteLine(n1); 

    }
}